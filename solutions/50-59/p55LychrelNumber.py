start = time.time()

def isPalindromic(num):
	mid = int(math.ceil(len(num) / 2.0))
	for i in range(mid):
		if not num[i] == num[-i-1]:
			return False
	return True

def reverse(num):
	r = ""
	for i in range(1, len(num)+1):
		r += num[-i]
	return int(r)

